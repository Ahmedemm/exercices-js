(() => {
    const targetElement = document.getElementById("target");
    const currentDate = new Date();

    const options = {
        weekday: 'long',
        day: 'numeric',
        month: 'long',
        year: 'numeric',
        hour: 'numeric',
        minute: 'numeric',
    };

    const formattedDate = currentDate.toLocaleDateString('fr-FR', options);
    targetElement.textContent = formattedDate;
})();
